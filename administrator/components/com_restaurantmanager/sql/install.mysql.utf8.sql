--
-- Table structure for menu items
--
CREATE TABLE IF NOT EXISTS `#__restaurantmanager_menu` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `item_number` int(11) NOT NULL,
  `item_name` varchar(200) NOT NULL,
  `item_price` decimal(10,2) NOT NULL,
  `item_description` text,
  `is_active` tinyint(1) NOT NULL DEFAULT 1,
  `ordering` int(11) NOT NULL DEFAULT 0,
  `checked_out` int(11) unsigned,
  `checked_out_time` datetime,
  `created` datetime NOT NULL,
  `created_by` int(11) unsigned NOT NULL DEFAULT 0,
  `modified` datetime,
  `modified_by` int(11) unsigned NOT NULL DEFAULT 0,
  PRIMARY KEY (`id`),
  UNIQUE KEY `idx_item_number` (`item_number`),
  KEY `idx_is_active` (`is_active`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 DEFAULT COLLATE=utf8mb4_unicode_ci;

--
-- Table structure for orders
--
CREATE TABLE IF NOT EXISTS `#__restaurantmanager_orders` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `table_number` int(11) NOT NULL,
  `item_number` int(11) NOT NULL,
  `quantity` int(11) NOT NULL DEFAULT 1,
  `notes` text,
  `order_date` datetime NOT NULL,
  `sent_to_kitchen` tinyint(1) NOT NULL DEFAULT 0,
  `sent_to_kitchen_date` datetime,
  `kitchen_status` varchar(20) NOT NULL DEFAULT 'pending',
  `kitchen_completed_date` datetime,
  `is_paid` tinyint(1) NOT NULL DEFAULT 0,
  `paid_date` datetime,
  `created_by` int(11) unsigned NOT NULL DEFAULT 0,
  `modified` datetime,
  `modified_by` int(11) unsigned NOT NULL DEFAULT 0,
  PRIMARY KEY (`id`),
  KEY `idx_table_number` (`table_number`),
  KEY `idx_is_paid` (`is_paid`),
  KEY `idx_order_date` (`order_date`),
  KEY `idx_item_number` (`item_number`),
  KEY `idx_sent_to_kitchen` (`sent_to_kitchen`),
  KEY `idx_kitchen_status` (`kitchen_status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 DEFAULT COLLATE=utf8mb4_unicode_ci;

